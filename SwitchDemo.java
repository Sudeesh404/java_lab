//java switch case demo
public class SwitchDemo
{
	public static void main(String[] args)
	{
		int x = 4;
		switch(x)
		{
			case 1:
					System.out.println("Violet");
					break;
			case 2:
					System.out.println("Red");
					break;
			default:
					System.out.println("invalid");
					break;
					
		}
	}
}